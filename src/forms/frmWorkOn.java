    private void btnRechercheActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRechercheActionPerformed
        String cherche ="select * from work_on where ";
        if (RadBtnNumEmp.isSelected()){ cherche+="empNo ="+TextRecherche.getText();}
        else if (RadBtnNomEmp.isSelected()){ cherche+="empNo ="+e.getValueByName(TextRecherche.getText());}
        else if (RadbtnNomPro.isSelected()){cherche+="proNo="+pro.getValueByName(TextRecherche.getText());}
        else { cherche+="proNo ='"+TextRecherche.getText()+"'";}
        w.getCustomRows(cherche, TblWork);
    }//GEN-LAST:event_btnRechercheActionPerformed
    WorkOn w = new WorkOn();
