    private void btnTrouverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTrouverActionPerformed
        // TODO add your handling code here:
        String strempNo = (String) Tools.Tools.InputBox("Entrer le Numéro d'employé");        
        String strproNo = (String) Tools.Tools.InputBox("Entrer le Numéro de projet");
    int empNo = Integer.parseInt(strempNo);
    int proNo = Integer.parseInt(strproNo);
    w.setEmpNo(empNo);
    w.setProNo(proNo);
    w.getOneRow(TblWork);
    }//GEN-LAST:event_btnTrouverActionPerformed

