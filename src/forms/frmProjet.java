    private void btnAdddeptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdddeptActionPerformed
        int deptNo = Integer.parseInt(d.getAutoNumber());
        String deptName = (String) Tools.Tools.InputBox("Entrer le nom de département");
        String location = (String) Tools.Tools.InputBox("Entrer la location de département");
        d.setDeptNo(deptNo);
        d.setDeptName(deptName);
        d.setLocation(location);
        d.add();
        db.go.fillCombo("dept", "deptName", TextproDept);
    }//GEN-LAST:event_btnAdddeptActionPerformed
    
    private void setValues(){
    pro.setProNo(Integer.parseInt(TextproNo.getText()));
    pro.setProName(TextproName.getText());
    pro.setLocation(TextproLocation.getText());
    pro.setDeptNo(Integer.parseInt(d.getValueByName((String) TextproDept.getSelectedItem())));
    }
