    private void btnRechercheActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRechercheActionPerformed
        String cherche ="select * from project where ";
        if (RadBtnNum.isSelected()){ cherche+="proNo ="+TextRecherche.getText();}
        else if (RadBtnNom.isSelected()){ cherche+="proName ='"+TextRecherche.getText()+"'";}
        else if (RdoDépartement.isSelected()){cherche+="deptNo="+TextRecherche.getText();}
        else { cherche+="location ='"+TextRecherche.getText()+"'";}
        pro.getCustomRows(cherche, tblPro);

    }//GEN-LAST:event_btnRechercheActionPerformed

