    private void tblProMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProMouseClicked
        // TODO add your handling code here:
         try{
         int row = tblPro.getSelectedRow();
         String proNo = tblPro.getValueAt(row, 0).toString();
         String proName = tblPro.getValueAt(row, 1).toString();
         String loc = tblPro.getValueAt(row, 2).toString();
         String deptNo = tblPro.getValueAt(row, 3).toString();
         TextproNo.setText(proNo);
         TextproName.setText(proName);
         TextproLocation.setText(loc);
         TextproDept.setSelectedItem(d.getNameByValue(deptNo));
         btnAjouter.setEnabled(false);
         btnModifier.setEnabled(true);
         btnSupprimer.setEnabled(true);
         }catch(Exception ex)
         {Message(ex.getMessage());} 
    }//GEN-LAST:event_tblProMouseClicked
    private void Clear(){
    Tools.Tools.ClearTexts(this);
    TextproNo.setText(pro.getAutoNumber());
    btnAjouter.setEnabled(true);
    btnModifier.setEnabled(false);
    btnSupprimer.setEnabled(false);
    TextproName.requestFocus();}
